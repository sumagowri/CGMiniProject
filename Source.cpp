void init(){
	glClearColor(0.0, 0.0, 0.0, 1.0);
	glClear(GL_COLOR_BUFFER_BIT);
	glMatrixMode(GL_PROJECTION);
	gluOrtho2D(0, width, 0, height);
}

//
void drawNode(int x,int y,int rad){
	glBegin(GL_POLYGON);
	for (double i = 0; i < 90 * PI / 2; i += PI / 6000) 
		glVertex3f(x + cos(i) * rad, y + sin(i) * rad, 0);
	glEnd();
}

void drawRoad(int x1, int y1, int x2, int y2){
	glBegin(GL_LINES);
	glVertex2f(x1,y1);
	glVertex2f(x2,y2);
	glEnd();
}

