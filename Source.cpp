int itr = 0;

void display(){
	glClear(GL_COLOR_BUFFER_BIT);

	glColor3f(1, 0, 0);
	for (auto &n : node){
		drawNode(n[0], n[1], rad);
	}

	for (int i = 0; i < 40; i++){
		for (int j = i + 1; j < 40; j++){
			if (conn[i][j] == 1) drawRoad(node[i][0], node[i][1], node[j][0], node[j][1]);
		}
	}

	if (press){
		int temp = ans.size() - 1;
		if (curidx < temp){
			if (itr <= 0 || curidx<0){
				
				curidx++;
				printf("here 1 %d", curidx);
				if (curidx<temp && node[ans[curidx]][0] == node[ans[curidx + 1]][0]){
					itr = abs(node[ans[curidx]][1] - node[ans[curidx + 1]][1]) / 10;
				}
				else if (curidx<temp && node[ans[curidx]][1] == node[ans[curidx + 1]][1]){
					itr = abs(node[ans[curidx]][0] - node[ans[curidx + 1]][0]) / 10;
				}
				printf("here 2");
				curx = node[ans[curidx]][0];
				cury = node[ans[curidx]][1];
			}
			else{
				itr--;
				if (node[ans[curidx]][0] == node[ans[curidx + 1]][0]){
					cury += node[ans[curidx]][1] > node[ans[curidx + 1]][1]?-10:10;
				}
				else if (node[ans[curidx]][1] == node[ans[curidx + 1]][1]){
					curx += node[ans[curidx]][0] > node[ans[curidx + 1]][0] ? -10 : 10;
				}
			}
			glColor3f(1, 1, 1);
			drawNode(curx, cury, rad - 10);
		}
		else{
			press = false;
		}
	}
	
	glutPostRedisplay();
	glFlush();
	//if (x < 200 && y < 200){
	//	x++;
	//	y++;
	//}
}

